#include <unistd.h>

int main(int ac, char **av)
{
	char *str = av[1];
	int i = 0;
	if (ac == 2)
	{
		while (str[i + 1])
			i++;
		while (str[i])
		{
			write(1, &str[i], 1);
			i--;
		}
	}
	write(1, "\n", 1);
	return (0);
}
